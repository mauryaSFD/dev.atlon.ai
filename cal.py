import math

# Constants
print("Pi:", math.pi)
print("Euler's constant:", math.e)

# Basic arithmetic functions
print("Square root of 16:", math.sqrt(16))
print("Exponential function e^2:", math.exp(2))
print("Logarithm base 10 of 100:", math.log10(100))
print("Logarithm base e of 100:", math.log(100))

# Trigonometric functions
print("Sine of 30 degrees:", math.sin(math.radians(30)))
print("Cosine of 45 degrees:", math.cos(math.radians(45)))
print("Tangent of 60 degrees:", math.tan(math.radians(60)))

# Hyperbolic functions
print("Hyperbolic sine of 0:", math.sinh(0))
print("Hyperbolic cosine of 0:", math.cosh(0))
print("Hyperbolic tangent of 1:", math.tanh(1))

# Angular conversion
print("Convert 180 degrees to radians:", math.radians(180))
print("Convert pi radians to degrees:", math.degrees(math.pi))

# Other functions
print("Absolute value of -10:", math.fabs(-10))
print("Factorial of 5:", math.factorial(5))
print("Greatest common divisor of 24 and 36:", math.gcd(24, 36))
print("Ceiling of 4.2:", math.ceil(4.2))
print("Floor of 4.8:", math.floor(4.8))
print("Round 4.5 to the nearest integer:", round(4.5))
